package com.example.iitg_speech_lab.Model;

public class GroupsDataModel {

        String gid;
        String extra;


        public GroupsDataModel(String gid, String extra) {
            this.gid = gid;
            this.extra = extra;

        }




        public String getGID() {
            return gid;
        }

        public String getExtra() {
            return extra;
        }
}
